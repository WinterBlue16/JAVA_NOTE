
public class StringOperation {

	public static void main(String[] args) {
		System.out.println("Hello World".length()); //11
		System.out.println("Hello leezche ... bye.".replace("leezche", "egoing")); // leezche 자리에 egoing 삽입
		
		System.out.println("Hello [[[name]]] ... bye.".replace("[[[name]]]", "duru")); // [[[name]]]자리에  duru 삽입
	
	}

}
